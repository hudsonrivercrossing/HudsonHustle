import { readdir, writeFile } from "node:fs/promises";
import path from "node:path";

const rootDir = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
const configRoot = path.join(rootDir, "configs/hudson-hustle");
const outputPath = path.join(rootDir, "packages/game-data/src/generated-config-registry.ts");

function sortAsc(values) {
  return [...values].sort((a, b) => a.localeCompare(b));
}

function toImportBase(configPath) {
  return `../../../${configPath}`.replace(/\\/g, "/");
}

function pascalCase(text) {
  return text
    .replace(/(^\w|-\w)/g, (part) => part.replace("-", "").toUpperCase())
    .replace(/[^A-Za-z0-9]/g, "");
}

async function listSnapshotDirs(modeDir) {
  const names = await readdir(modeDir, { withFileTypes: true });
  return sortAsc(
    names
      .filter((entry) => entry.isDirectory())
      .map((entry) => entry.name)
  );
}

async function main() {
  const draftIds = await listSnapshotDirs(path.join(configRoot, "drafts"));
  const releaseIds = await listSnapshotDirs(path.join(configRoot, "releases"));

  const entries = [
    ...draftIds.map((id) => ({
      id,
      mode: "draft",
      configPath: `configs/hudson-hustle/drafts/${id}`
    })),
    ...releaseIds.map((id) => ({
      id,
      mode: "release",
      configPath: `configs/hudson-hustle/releases/${id}`
    }))
  ];

  const importLines = [];
  const objectEntries = [];

  for (const entry of entries) {
    const importBase = toImportBase(entry.configPath);
    const varBase = `${entry.mode}${pascalCase(entry.id)}`;

    importLines.push(`import ${varBase}Map from "${importBase}/map.json";`);
    importLines.push(`import ${varBase}Meta from "${importBase}/meta.json";`);
    importLines.push(`import ${varBase}Rules from "${importBase}/rules.json";`);
    importLines.push(`import ${varBase}Tickets from "${importBase}/tickets.json";`);
    importLines.push(`import ${varBase}Visuals from "${importBase}/visuals.json";`);

    objectEntries.push(`  "${entry.id}": {
    configId: "${entry.id}",
    configPath: "${entry.configPath}",
    mode: "${entry.mode}",
    meta: ${varBase}Meta as SnapshotMeta,
    map: ${varBase}Map as SnapshotMap,
    tickets: ${varBase}Tickets as SnapshotTickets,
    rules: ${varBase}Rules as SnapshotRules,
    visuals: ${varBase}Visuals as SnapshotVisuals
  }`);
  }

  const content = `/* eslint-disable */
// This file is generated by scripts/generate-config-registry.mjs.
// Edit the config snapshots, then regenerate this file instead of hand-editing it.

${importLines.join("\n")}
import type { RegisteredConfigBundle, SnapshotMap, SnapshotMeta, SnapshotRules, SnapshotTickets, SnapshotVisuals } from "./config-types";

export const generatedHudsonHustleConfigRegistry: Record<string, RegisteredConfigBundle> = {
${objectEntries.join(",\n")}
};
`;

  await writeFile(outputPath, content);
  console.log(
    JSON.stringify(
      {
        generated: true,
        outputPath: "packages/game-data/src/generated-config-registry.ts",
        configs: entries.map((entry) => entry.configPath)
      },
      null,
      2
    )
  );
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
